var paciente = document.querySelector('#primeiro-paciente');

var infohtml = paciente.querySelector('.infohtml');
var html = parseInt(infohtml.textContent);
console.log (html);

var infocss = paciente.querySelector('.infocss');
var css = parseInt(infocss.textContent);
console.log (css);

var infojavascript = paciente.querySelector('.infojavascript');
var javascript = parseInt(infojavascript.textContent);
console.log (javascript);

var infomedia  = (html + css + javascript) / 3
console.log (infomedia)
var media = paciente.querySelector('.infomedia')
media.textContent = infomedia

